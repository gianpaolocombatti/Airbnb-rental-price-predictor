def load_reviews(dir_value):
    """loads reviews text dataframe"""
    alpha = review_text_directory[dir_value]
    alpha = alpha.strip('"')
    return pd.read_csv(alpha)


def add_scores(df, df_r):
    """adds review scores to dataframe"""
    df_copy = df_r.copy()

    for x in range(len(df)):
        y = df.iloc[x]['review_scores_rating']
        z = df.iloc[x]['id']
        df_copy.loc[df_copy['listing_id'] == z, 'review_score'] = y

    return(df_copy)
